#include<stdio.h>

int main(){    
   int c=3; //---%d is used for integers
   char b='h'; //---%c is used for only one character
   float a=3.14;  //---%f is used for floating point numbers
   printf("The output is %d\n", c);
   printf("the output is %c\n", b);
   printf("The output is %f\n", a);
   return 0;//orders matter        
}   